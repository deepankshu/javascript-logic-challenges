// Favorite Colors Access
// Demonstrates how to access elements in an array using indexes.
// The program prints the first color, the last color, and the total number of colors.

// Example Array: ['Red', 'Yellow', 'Green', 'Pink', 'Purple']
// Expected Output:
// First color: Red
// Last color: Purple
// Total colors: 5

const colors = ['Red', 'Yellow', 'Green', 'Pink', 'Purple'];
console.log("First color:", colors[0]);
console.log("Last color:", colors[colors.length - 1]);
console.log("Total colors:", colors.length);